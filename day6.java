public class day6 {
    public static void main(String[] args) {
        /*
         float  → tipe data untuk bilangan pecahan (desimal) 
                  dengan presisi 6–7 digit. Lebih hemat memori (4 byte).

         double → tipe data untuk bilangan pecahan (desimal) 
                  dengan presisi 15–16 digit. Lebih akurat, ukuran 8 byte.
        */

        // Contoh penggunaan float
        float tinggiBadan = 165.5f;
        System.out.println("Tinggi badan saya : " + tinggiBadan + " cm");

        // Contoh penggunaan double
        double gajiBulanan = 4500000.75;
        System.out.println("Gaji bulanan      : Rp " + gajiBulanan);
    }
}
